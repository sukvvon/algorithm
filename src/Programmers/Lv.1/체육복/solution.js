function solution(n, lost, reserve) {
  const students = Array.from({ length: n });
  students.fill(1);

  reserve.forEach((value) => {
    students[value - 1]++;
  });

  lost.forEach((value) => {
    students[value - 1]--;
  });

  for (let i = 0; i < students.length; i++) {
    if (students[i] === 0) {
      if (students[i - 1] === 2) {
        students[i - 1]--;
        students[i]++;
      } else if (students[i + 1] === 2) {
        students[i + 1]--;
        students[i]++;
      }
    }
  }

  return students.filter((value) => value > 0).length;
}
